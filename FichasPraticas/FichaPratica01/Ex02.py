num1 = int(input("Insira um número: "))

num2 = int(input("Insira outro número: "))

# Soma
resultado = num1 + num2
print("Soma:", resultado)

# Subtração
resultado = num1 - num2
print("Subtração:", resultado)

# Multiplicação
resultado = num1 * num2
print("Multiplicação:", resultado)

# Divisão
resultado = num1 / num2
print("Divisão:", resultado)
